package lacos0905.exer4;

public class Pessoa {
	private int idade;
	private int sexo;
	private int personalidade;

	public Pessoa(int idade, int sexo, int personalidade) {
		this.idade = idade;
		this.sexo = sexo;
		this.personalidade = personalidade;
	}

	public int getIdade() {
		return idade;
	}

	public int getSexo() {
		return sexo;
	}

	public int getPersonalidade() {
		return personalidade;
	}

}
